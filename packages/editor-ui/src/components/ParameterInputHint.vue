<template>
	<n8n-text size="xsmall" color="text-base" tag="div" v-if="hint">
		<div ref="hint" :class="$style.hint" v-html="hint"></div>
	</n8n-text>
</template>

<script lang="ts">
import Vue from "vue";

export default Vue.extend({
	name: 'InputHint',
	props: ['hint'],
	mounted(){
		if(this.$refs.hint){
			(this.$refs.hint as Element).querySelectorAll('a').forEach(a => a.target = "_blank");
		}
	},
});
</script>


<style lang="scss" module>
	.hint {
		white-space: nowrap;
		overflow: hidden;
		text-overflow: ellipsis;
	}
</style>


